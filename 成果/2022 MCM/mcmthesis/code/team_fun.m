eqns=[x(1)-(k*rate(1)*x(8)^3+m*g*Crr*x(8)),...
    x(2)-(k*rate(2)*x(8)^3+m*g*Crr*x(8)),...
    x(3)-(k*rate(3)*x(8)^3+m*g*Crr*x(8)),...
    x(4)-(k*rate(4)*x(8)^3+m*g*Crr*x(8)),...
    x(5)-(k*rate(5)*x(8)^3+m*g*Crr*x(8)),...
    x(6)-(k*rate(6)*x(8)^3+m*g*Crr*x(8)),...
    x(7)-(k*(x(8)-1.17)^3+m*g*Crr*(x(8)-1.17)),...
    (x(1)+x(3)+x(5)-3*CP_High)*20+(x(7)-CP_High)*10+(x(6)-CP_High)*...
    (x(9)-10)+(x(4)+x(2)-2*CP_High)*x(9),...
    (x(1)+x(3)+x(5)-3*CP_Low)*x(9)+(x(7)-CP_Low)*10+(x(6)-CP_Low)*...
    10+(x(4)+x(2)-2*CP_Low)*10];
